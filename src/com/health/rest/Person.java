package com.health.rest;

public class Person {
	
	private String name;
	private int gender;
	private String id;
	private float oldpeak;
	private String email;
	private int age;
	private String contactNumber;
	private int cheastpain;		
	private int restingbloodpressure;
	private int maxHeartRate;
	
	public int getMaxHeartRate() {
		return maxHeartRate;
	}
	public void setMaxHeartRate(int maxHeartRate) {
		this.maxHeartRate = maxHeartRate;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getGender() {
		return gender;
	}
	public void setGender(int gender) {
		this.gender = gender;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public float getOldpeak() {
		return oldpeak;
	}
	public void setOldpeak(float oldpeak) {
		this.oldpeak = oldpeak;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getContactNumber() {
		return contactNumber;
	}
	public void setContactNumber(String contactNumber) {
		this.contactNumber = contactNumber;
	}
	public int getCheastpain() {
		return cheastpain;
	}
	public void setCheastpain(int cheastpain) {
		this.cheastpain = cheastpain;
	}
	public int getRestingbloodpressure() {
		return restingbloodpressure;
	}
	public void setRestingbloodpressure(int restingbloodpressure) {
		this.restingbloodpressure = restingbloodpressure;
	}
	
	

}
